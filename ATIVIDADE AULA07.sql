#12
SELECT
	SUM(VALOROPERACAO) 'Soma do valor total',
   	NOMETIPOOPERACAO 'Tipo da operação',
    	NUMEROCONTA 'Número da conta',
    	FKIDTB_AGENCIA 'Número da agência'
