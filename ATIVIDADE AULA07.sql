SELECT
	IDTB_OPERACAO,
    	DTOPERACAO,
    	VALOROPERACAO,
    	NOMETIPOOPERACAO
FROM tb_operacao
INNER JOIN tb_tipooperacao
ON IDTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
WHERE ValorOperacao > 200 AND month(DTOPERACAO) = 12
group by idTB_OPERACAO, YEAR(DTOPERACAO);

#15
SELECT
	COUNT(IDTB_OPERACAO),
    	SUM(VALOROPERACAO),
    	AVG(VALOROPERACAO),
    	NOMETIPOOPERACAO,
    	concat(MONTHNAME(DTOPERACAO),'/',YEAR(DTOPERACAO)) 'MÊS/ANO'
FROM TB_OPERACAO
INNER JOIN tb_tipooperacao
ON IDTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
WHERE NOMETIPOOPERACAO = 'Depósito'
group by 5;

#16
SELECT
	IDTB_OPERACAO,
    	DTOPERACAO,
   	VALOROPERACAO,
    	NOMETIPOOPERACAO
FROM tb_operacao
INNER JOIN tb_tipooperacao
ON IDTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
WHERE NomeTipoOperacao = 'Depósito' 
and ValorOperacao = (
	SELECT 
		MAX(ValorOperacao) 
	FROM TB_OPERACAO 
    	INNER JOIN tb_tipooperacao
	ON IDTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
    	WHERE NomeTipoOperacao = 'Depósito');
    

#17
SELECT
	round(AVG(VALOROPERACAO),2),
	NOMETIPOOPERACAO,
    	DTOPERACAO
FROM TB_OPERACAO
INNER JOIN tb_tipooperacao
ON IDTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
WHERE NomeTipoOperacao = 'Depósito'
	AND day(DTOPERACAO) between 5 AND 15
group by concat(MONTH(DTOPERACAO),'/',YEAR(DTOPERACAO));

#18
SELECT 
	IDTB_OPERACAO,
	DTOPERACAO,
    	VALOROPERACAO,
    	NOMETIPOOPERACAO,
    	NUMEROCONTA,
    	FKIDTB_AGENCIA
FROM TB_OPERACAO
INNER JOIN tb_tipooperacao
ON idTB_TIPOOPERACAO = FKIDTB_TIPOOPERACAO
INNER JOIN TB_CONTA
ON IDTB_CONTA = FKIDTB_CONTA
WHERE NUMEROCONTA = 13321 
	AND FKIDTB_AGENCIA = 4 
	AND NomeTipoOperacao = 'Depósito' 
	AND VALOROPERACAO = (
		SELECT
			MAX(VALOROPERACAO)
		FROM TB_OPERACAO
		INNER JOIN TB_TIPOOPERACAO
		ON idTB_TIPOOPERACAO = FKidTB_TIPOOPERACAO
		INNER JOIN TB_CONTA
		ON IDTB_CONTA = FKIDTB_CONTA
		WHERE NUMEROCONTA = 13321
			AND FKIDTB_AGENCIA = 4
            		AND NOMETIPOOPERACAO = 'Depósito');
            
